import http from 'k6/http';
import { check, sleep } from 'k6';
import { Rate, Trend } from 'k6/metrics';

const API_KEY = __ENV.API_KEY || 'dev-key-change-in-production';
const BASE_URL = __ENV.BASE_URL || 'http://localhost:8080';

const ingestErrorRate = new Rate('ingest_errors');
const dashboardErrorRate = new Rate('dashboard_errors');
const ingestDuration = new Trend('ingest_duration', true);
const dashboardDuration = new Trend('dashboard_duration', true);

export const options = {
    stages: [
        { duration: '30s', target: 100 },
        { duration: '60s', target: 100 },
        { duration: '10s', target: 0 },
    ],
    thresholds: {
        ingest_duration: ['p(95)<1000'],
        dashboard_duration: ['p(95)<200'],
        ingest_errors: ['rate<0.01'],
        dashboard_errors: ['rate<0.01'],
    },
};

const headers = {
    'Content-Type': 'application/json',
    'X-API-Key': API_KEY,
};

function randomModel() {
    const models = ['gpt-4o', 'gpt-4o-mini', 'claude-3-5-sonnet-20241022'];
    return models[Math.floor(Math.random() * models.length)];
}

export default function () {
    // POST /api/interactions
    const now = new Date();
    const started = new Date(now.getTime() - 500 - Math.random() * 2000);
    const payload = JSON.stringify({
        projectName: 'k6 Load Test Project',
        projectDescription: 'Auto-generated by k6',
        experimentName: 'Load Test Experiment',
        iterationName: `Run ${Math.floor(Math.random() * 5) + 1}`,
        model: randomModel(),
        prompt: 'k6 test prompt ' + Math.random(),
        tokensIn: Math.floor(100 + Math.random() * 500),
        tokensOut: Math.floor(50 + Math.random() * 300),
        startedAt: started.toISOString(),
        endedAt: now.toISOString(),
    });

    const ingestRes = http.post(`${BASE_URL}/api/interactions`, payload, { headers });
    ingestDuration.add(ingestRes.timings.duration);
    const ingestOk = check(ingestRes, { 'ingest 201': (r) => r.status === 201 });
    ingestErrorRate.add(!ingestOk);

    sleep(0.1);

    // GET /api/dashboard/summary
    const summaryRes = http.get(`${BASE_URL}/api/dashboard/summary`, { headers });
    dashboardDuration.add(summaryRes.timings.duration);
    const summaryOk = check(summaryRes, { 'summary 200': (r) => r.status === 200 });
    dashboardErrorRate.add(!summaryOk);

    // GET /api/dashboard/timeline
    const timelineRes = http.get(`${BASE_URL}/api/dashboard/timeline`, { headers });
    dashboardDuration.add(timelineRes.timings.duration);
    dashboardErrorRate.add(!check(timelineRes, { 'timeline 200': (r) => r.status === 200 }));

    // GET /api/dashboard/tokens-by-model
    const tokensRes = http.get(`${BASE_URL}/api/dashboard/tokens-by-model`, { headers });
    dashboardDuration.add(tokensRes.timings.duration);
    dashboardErrorRate.add(!check(tokensRes, { 'tokens 200': (r) => r.status === 200 }));

    // GET /api/dashboard/latency-distribution
    const latencyRes = http.get(`${BASE_URL}/api/dashboard/latency-distribution`, { headers });
    dashboardDuration.add(latencyRes.timings.duration);
    dashboardErrorRate.add(!check(latencyRes, { 'latency 200': (r) => r.status === 200 }));

    // GET /api/dashboard/tool-usage
    const toolRes = http.get(`${BASE_URL}/api/dashboard/tool-usage`, { headers });
    dashboardDuration.add(toolRes.timings.duration);
    dashboardErrorRate.add(!check(toolRes, { 'tool-usage 200': (r) => r.status === 200 }));

    sleep(0.5);
}
